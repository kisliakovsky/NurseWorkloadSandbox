package ru.ifmo.escience.compbiomed.sandbox.sensor;

public class Attractor {

    private double x;
    private double y;

    public Attractor() {
        this.x = 5.0;
        this.y = 8.0;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
