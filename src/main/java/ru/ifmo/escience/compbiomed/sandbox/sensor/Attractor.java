package ru.ifmo.escience.compbiomed.sandbox.sensor;

public class Attractor {

    private double x;
    private double y;

    public Attractor(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
